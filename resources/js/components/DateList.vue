<template>
    <div>
        <swiper :options="swiperOption">
            <div class="swiper-scrollbar" slot="scrollbar"></div>
            <swiper-slide class=""
                          v-for="(day, key) in dateList"
                          :key="key"
            >

                <div class="date pt-3">{{day.week_day}}</div>
                <hr class="my-1 mx-4">
                <div class="list-group" >
                    <div class="mx-2 my-1 date" v-for="hour in day.hours">
                        <div class="p-1 list-group-item list-group-item-action pointer" data-entity="hour"
                            :data-disabled="hour.disable"
                            :data-date="hour.date"
                            :disabled="isDisabled(hour.disable)"
                            :class="{selected: isCheckDate(hour.date), disabled: isDisabled(hour.disable)}"
                            @click="checkDate(hour.date)"
                        >
                            {{hour.start_time}}
                            <!--                        - {{hour.end_time}}-->
                        </div>
                    </div>

                </div>
            </swiper-slide>
        </swiper>
    </div>
</template>

<script>
    export default {
        props: [
            'dates',
        ],
        data() {
            return {
                dateList: [],
                date: '',
                weekIndex: 0,
                day: null,
                hour: null,
                swiperOption: {
                    slidesPerView: 7,
                    spaceBetween: 0,
                    slidesPerGroup: 7,
                    loop: true,
                    loopFillGroupWithBlank: true,
                    scrollbar: {
                        el: '.swiper-scrollbar',
                        // hide: true
                    },
                },
            }
        },
        mounted() {
            this.update();
        },
        methods: {
            update: function () {
                    this.dateList = this.dates;
            },
            isDisabled(disable){
                return ( disable == 1 );
            },
            checkDate(date){
                debugger;
                this.date = date;
                // console.log(this.date);
                this.$emit('return', this.date);
            },
            isCheckDate(date){
                return this.date == date;
            },
        }
    }
</script>

